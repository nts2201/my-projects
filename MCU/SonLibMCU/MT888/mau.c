#include <REGX52.H>

void init()
{
delay (5000);
P2=0xFF;delay(200);
CS=0;
delay(200);

P2=0xE0;delay(200);
CS=0;
delay(200);

P2=0xE0;delay(200);
CS=0;
delay(200);

P2=0xE8;delay(200);
CS=0;
delay(200);

P2=0xE0;delay(200);
CS=0;
delay(200);

P2=0xFF;delay(200);
CS=0;
delay(200);

CS=1;
delay(200);
}

//================================================== ===================

void ngat()
{
P2=0xE4;	 //cs =1
delay(200);
CS=0;		 //cs=0
delay(7000);
CS=1;
delay(200);
}

//================================================== ===================

void setup()
{
P2=0xE9;
delay(200);
CS=0;
delay(200);
P2=0xEB;
delay(200);
CS=0;
delay(200);
CS=1;
delay(200);
}

